const DEFAULT_LANGUAGE: Language = process.env.EXPO_PUBLIC_DEFAULT_LANGUAGE === "pt" ? "pt" : process.env.EXPO_PUBLIC_DEFAULT_LANGUAGE === "es" ? "es" : "en";
